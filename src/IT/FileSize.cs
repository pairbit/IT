namespace IT
{
    /// <summary>
    /// Размер файла
    /// </summary>
    public enum FileSize
    {
        /// <summary>
        /// Байт
        /// </summary>
        B = 1,

        /// <summary>
        /// Килобайт
        /// </summary>
        KB = 1024,

        /// <summary>
        /// Мегабайт
        /// </summary>
        MB = KB * 1024,

        /// <summary>
        /// Гигабайт
        /// </summary>
        GB = MB * 1024
    }
}