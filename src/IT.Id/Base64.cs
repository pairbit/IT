                                                 '8', '9', '-', '_', '=' };

    public static Byte[] ToBytes(String base64)
    {
        var repeat = base64.Length & 3;
        if (repeat > 0) repeat = 4 - repeat;
        return Convert.FromBase64String(new StringBuilder(base64).Replace('_', '/').Replace('-', '+').Append('=', repeat).ToString());
    }
